    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        try {
            // TODO add your handling code here:
            String sir1 = ""; //Decriptarea - se schimba pathul catre fisier
            try {
                FileWriter ciphertextWriter = new FileWriter("E:\\Tema1Cripto\\DublaCriptare\\src\\dublacriptare\\cipheredtext.txt");
                ciphertextWriter.write(jTextArea7.getText());
                ciphertextWriter.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
            File file1 = new File("E:\\Tema1Cripto\\DublaCriptare\\src\\dublacriptare\\cipheredtext.txt");
            Scanner scanner1 = new Scanner(file1);

            while (scanner1.hasNextLine()) {
                String content = scanner1.nextLine();
                sir1 = sir1 + content;
            }
           
            
            String sirReplaced = sir1.replaceAll("[^1-5]","");
            StringBuffer polybiusStringBuffer = convertStringToStringBuffer(sirReplaced);
            int shift = jSlider2.getValue();

            StringBuffer cezarDecriptat = caesarDecipher(decryptPolybius(polybiusStringBuffer), 26 - shift);
            jTextArea5.setText(cezarDecriptat.toString());

            try {
                FileWriter decipherWriter = new FileWriter("E:\\Tema1Cripto\\DublaCriptare\\src\\dublacriptare\\decipher.txt");
                decipherWriter.write(cezarDecriptat.toString());
                decipherWriter.close();
            } catch (IOException e) {
                e.printStackTrace();
            }

            scanner1.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Criptare.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_button2ActionPerformed

