    public static StringBuffer polybiusCipher(String plainText) {
        StringBuffer result = new StringBuffer();
        plainText = plainText.toUpperCase();
        plainText = plainText.replaceAll(" ", "");
        plainText = plainText.replaceAll("J", "I");

        char allchar[][] = {{'A', 'B', 'C', 'D', 'E'}, {'F', 'G', 'H', 'I', 'K'},
        {'L', 'M', 'N', 'O', 'P'}, {'Q', 'R', 'S', 'T', 'U'}, {'V', 'W', 'X', 'Y', 'Z'}};
        int len = plainText.length();
        char b;
        for (int i = 0; i < len; i++) {
            b = plainText.charAt(i);
            for (int j = 0; j < 5; j++) {
                for (int k = 0; k < 5; k++) {
                    if (b == allchar[j][k]) {
                        int f = j + 1;
                        int s = k + 1;

                        result.append(f).append("").append(s);
                    }
                }
            }
        }
        return result;
    }

    public static StringBuffer caesarCipher(String text, int s) {
        StringBuffer result = new StringBuffer();
        for (int i = 0; i < text.length(); i++) {
            if (Character.isUpperCase(text.charAt(i))) {
                char ch = (char) (((int) text.charAt(i)
                        + s - 65) % 26 + 65);
                result.append(ch);
            } else {
                char ch = (char) (((int) text.charAt(i)
                        + s - 97) % 26 + 97);
                result.append(ch);
            }
        }
        return result;
    }

    public static StringBuffer caesarDecipher(String text, int s) {
        StringBuffer result = new StringBuffer();
        for (int i = 0; i < text.length(); i++) {
            if (Character.isUpperCase(text.charAt(i))) {
                char ch = (char) (((int) text.charAt(i)
                        + s - 65) % 26 + 65);
                result.append(ch);
            } else {
                char ch = (char) (((int) text.charAt(i)
                        + s - 97) % 26 + 97);
                result.append(ch);
            }
        }
        return result;
    }

    public static String decryptPolybius(StringBuffer crypt) {
        int len = crypt.length();
        StringBuilder plain = new StringBuilder(len / 2);
        char allchar[][] = {{'A', 'B', 'C', 'D', 'E'}, {'F', 'G', 'H', 'I', 'K'},
        {'L', 'M', 'N', 'O', 'P'}, {'Q', 'R', 'S', 'T', 'U'}, {'V', 'W', 'X', 'Y', 'Z'}};

        for (int i = 0; i < len; i += 2) {
            int rowIndex = crypt.charAt(i) - '0' - 1;
            int colIndex = crypt.charAt(i + 1) - '0' - 1;
            plain.append(allchar[rowIndex][colIndex]);
        }

        return plain.toString();
    }

